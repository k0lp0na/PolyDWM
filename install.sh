#!/bin/bash

# Error handling function
handle_error() {
    echo "An error occurred on line $1"
    exit 1
}

# Trap errors
trap 'handle_error $LINENO' ERR

# Update system
echo "Updating system..."
sudo pacman -Syu --noconfirm

# Init paru
echo "Cloning paru repository..."
if ! git clone https://aur.archlinux.org/paru.git; then
    echo "Failed to clone paru repository"
    exit 1
fi

cd paru || exit
echo "Building and installing paru..."
makepkg -si --noconfirm
cd .. || exit

# Clean up paru directory
echo "Cleaning up paru directory..."
rm -rf paru

# Create symbolic links
echo "Creating symbolic links..."
ln -sf ~/DWM/dots/.dwm ~/
ln -sf ~/DWM/dots/alacritty ~/.config/
ln -sf ~/DWM/dots/dunst ~/.config/
ln -sf ~/DWM/dots/fish ~/.config/
ln -sf ~/DWM/dots/picom ~/.config/
ln -sf ~/DWM/dots/polybar ~/.config/
ln -sf ~/DWM/dots/rofi ~/.config/
ln -sf ~/DWM/dots/sxhkd ~/.config/
ln -sf ~/DWM/dots/fastfetch ~/.config/
ln -sf ~/DWM/dots/mpv ~/.config/
ln -sf ~/DWM/dots/omp ~/.config/
ln -sf ~/DWM/dots/kitty ~/.config/
ln -sf ~/DWM/dots/yazi ~/.config/

# Install packages using pacman
echo "Installing packages..."
sudo pacman -S --noconfirm alacritty rofi dunst picom sxhkd fish lsd fzf zoxide nitrogen firefox xorg xorg-xsetroot nemo maim xclip vulkan-intel fastfetch ttf-jetbrains-mono-nerd lxappearance qt5ct qt6ct mpv xed qbittorrent playerctl pacman-contrib noto-fonts noto-fonts-cjk noto-fonts-emoji noto-fonts-extra rofi-emoji vlc pipewire pipewire-alsa pipewire-pulse pipewire-jack wireplumber polkit-gnome xfce4-power-manager kitty yazi htop

# Install AUR packages using paru
echo "Installing AUR packages..."
paru -S --noconfirm polybar-dwm-git pfetch-rs-bin betterlockscreen beautyline phinger-cursors rofi-greenclip brave-bin vesktop-bin

# Build and install DWM
echo "Building and installing DWM..."
cd dwm || exit
sudo make clean install
rm -f drw.o dwm.o dwm-msg.o util.o config.h
cd .. || exit

# Create screenshots directory
echo "Creating screenshots directory..."
mkdir -p ~/Pictures/ss

# Enable audio services
echo "Enabling audio services..."
systemctl --user --now enable pipewire pipewire-pulse wireplumber

# Change default shell to fish
echo "Changing default shell to fish..."
chsh -s "$(which fish)"

# Remove VLC if it's not needed
echo "Removing VLC..."
sudo pacman -R --noconfirm vlc i3-wm

# Install and enable SDDM
echo "Installing and enabling SDDM..."
sudo pacman -S --noconfirm sddm
sudo systemctl enable sddm

# Setup Python virtual environment
echo "Setting up Python virtual environment..."
sudo pacman -S --noconfirm python-pip
python -m venv ~/myenv
source myenv/bin/activate
pip install anipy-cli

#done 


